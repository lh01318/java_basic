class IntegerExample {
	public static void main(String[] args) {
		// 정수의 다양한 표기
		int num1 = 128; // 10진수 표기
		int num2 = 0b0101; // 2진수 표기
		int num3 = 007; // 8진수 표기
		int num4 = 0x10AB; // 16진수 표기
	
		System.out.println(num1);
		System.out.println(num2);
		System.out.println(num3);
		System.out.println(num4);
		
		long balace = 11111111111L; // 롱타입쓸때는 뒤에 L을 써준다.
		byte x = -128;
		// byte y = 128;

		// 실수 표현
		float pi = 3.14F; // float 타입을 표현하는 접미사 F
		double pi2 = 3.1433333333333333333333333333333333; // 짤림
		System.out.println(pi2);


		


	}
}
